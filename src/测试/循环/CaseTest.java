package 测试.循环;

public class CaseTest {
    public static void main(String[] args) {
        String a = "2";
        switch(a){
            case "1" :
                //语句
                System.out.println("1");
                break; //可选
            case "2"  :
                //语句
                System.out.println("2");
                break; //可选
            //你可以有任意数量的case语句
            default : //可选
                //语句
        }
    }
}
